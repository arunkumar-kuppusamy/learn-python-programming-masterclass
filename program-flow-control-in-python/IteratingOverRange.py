# Iterating over a range

for num in range(1, 21):    # range is also UP TO BUT NOT INCLUDING the end value
    print(f'i is now {num}')

print()


for num in range(0, 21, 2):  # iterating with step 2
    print(f'i is now {num}')

print()

for num in range(10, 0, -2):  # iterating with step 2 backwards
    print(f'i is now {num}')